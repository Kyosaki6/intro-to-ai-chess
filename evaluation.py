def evaluate_board(board: chess.Board) -> float:
    score = 0
    for square in chess.SQUARES:
        piece = board.piece_at(square)
        if piece is None:
            continue
        piece_type = piece.piece_type
        value = PIECE_VALUES[piece_type]
        table = PIECE_TABLES[piece_type]
        if piece.color == chess.WHITE:
            # White's perspective: rank 0 is rank 8, rank 7 is rank 1
            # For white, square index maps directly (rank 0 = 8th rank)
            pos_score = table[square]
            score += value + pos_score
        else:
            # Flip the square for black perspective
            flipped = chess.square_mirror(square)
            pos_score = table[flipped]
            score -= value + pos_score
    return score
